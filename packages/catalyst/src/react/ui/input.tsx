import * as React from 'react';
import { cn } from '../lib/utils';

export interface InputProps
  extends React.InputHTMLAttributes<HTMLInputElement> {}

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          'flex h-9 w-full rounded-[var(--catalyst-radius-sm)] border border-[var(--catalyst-border-input)] bg-[var(--catalyst-background)] px-3 py-1 text-sm text-[var(--catalyst-foreground)] shadow-sm transition-colors placeholder:text-[var(--catalyst-muted-foreground)] focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[var(--catalyst-ring)] disabled:cursor-not-allowed disabled:opacity-50',
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);
Input.displayName = 'Input';

export { Input };
